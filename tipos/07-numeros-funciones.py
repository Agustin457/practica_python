""" El modulo de math, donde encontramos todas la funciones https://docs.python.org/3/library/math.html """
# Estamos importando una libreria de matematica para python 
import math

# Rouen metodo utilizado para redondear numeros / variables
print(round(1.3))
print(round(1.7))
print(round(1.5))
# abs nos devuelve el valor absoluto del numero/var que nosotros coloquemos
print(abs(-77))
print(abs(55))

# Modulo math

print(math.ceil(1.1)) # Numero entero mas cercano hacia arriva
print(math.floor(1.99999)) # Numero entero mas cercano hacia abajo
print(math.isnan(23)) # SI el valor que le estamso pasando un numero o no
# si el valor es un numero sera falso, solo numero reales
print(math.pow(10,3)) # Eleva en este caso 10**3
print(math.sqrt(9)) # raiz cuadrada