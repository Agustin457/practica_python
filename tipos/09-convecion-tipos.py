# bool() puede tener objetos verdaderos o falsos, los falsos son; 0, que no alla dato y objeto none
print(bool("")) # al no haber dato es falso
print(bool("0")) # como tiene un dato es verdadero y es str
print(bool(None)) # obejeto none por lo tanto falso
print(bool(" ")) # tiene un espacio por lo tanto verdadero(str)
print(bool(0)) # flaso porque al ser cero int 
